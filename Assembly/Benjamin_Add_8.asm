DATA SEGMENT
PROMPT1 DB 0AH, "ENTER THE FIRST NUMBER:$"
PROMPT2 DB 0AH, "ENTER THE SECOND NUMBER:$"
RESULT DB 0AH, "THE RESULT IS: $"
DATA ENDS

CODE SEGMENT
START:
MOV AX, DATA
MOV DS, AX
MOV DX, OFFSET PROMPT1
MOV AH, 09H
INT 21H

MOV AH, 01H
INT 21H
SUB AL, 30H
MOV BL, 0AH
MUL BL
MOV BH, AL

MOV AH, 01H
INT 21H
SUB AL, 30H
MOV AH, BH
ADD AH, AL
MOV BH, AH

MOV DX, OFFSET PROMPT2
MOV AH, 09H
INT 21H

MOV AH, 01H
INT 21H
SUB AL, 30H
MOV BL, 0AH
MUL BL
MOV CH, AL

MOV AH, 01H
INT 21H
SUB AL, 30H
MOV AH, CH
ADD AH, AL
MOV CH, AH

MOV DX, OFFSET RESULT
